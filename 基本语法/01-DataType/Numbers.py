if __name__ == '__main__':
        # 整数
        age=100
        print('age',age)
        # 打印变量的类型
        print(type(age))
        # 打印变量的内存地址
        print(id(age))
        # 浮点数
        salary=100.0
        print('salary',salary)
        # 打印变量的类型
        print(type(salary))
        # 打印变量的内存地址
        print(id(salary))
        # 复数
        c=1+2j
        print('c',c)
        # 打印变量的类型
        print(type(c))
        # 打印变量的内存地址
        print(id(c))
        # 布尔值
        flag=True
        print('flag',flag)
        # 打印变量的类型
        print(type(flag))
        # 打印变量的内存地址
        print(id(flag))
