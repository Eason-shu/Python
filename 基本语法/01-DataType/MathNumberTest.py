import math

if __name__ == '__main__':
    # 取余
    print(math.fmod(10, 3))
    # 向上取整
    print(math.ceil(10.1))
    # 向下取整
    print(math.floor(10.9))
    # 四舍五入
    print(round(10.5))
    # 绝对值
    print(math.fabs(-10))
    # 幂运算
    print(math.pow(10, 2))
    # 平方根
    print(math.sqrt(100))
    # 三角函数
    print(math.sin(10))
